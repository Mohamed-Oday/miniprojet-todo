package org.openjfx.miniprojet.model;

/**
 * Enum representing the status of a task.
 */
public enum Status {
    /**
     * The task has been started.
     */
    Started,

    /**
     * The task has been completed.
     */
    Completed,

    /**
     * The task has been abandoned.
     */
    Abandoned,

    /**
     * The task is pending.
     */
    Pending
}